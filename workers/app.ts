export default {
  async fetch(
    request: Request,
    env: Env,
    ctx: ExecutionContext
  ): Promise<Response> {
    const url = new URL(request.url);
    if (url.pathname.startsWith("/container")) {
      // Get the container instance for a given ID.
      // Here, we'll use a fixed ID for simplicity, but you could use something from the request
      const id = env.MY_CONTAINER.idFromName("default-instance");
      const container = env.MY_CONTAINER.get(id);
      return await container.fetch(request);
    }
    return requestHandler(request, {
      cloudflare: { env, ctx },
    });
  },
};

